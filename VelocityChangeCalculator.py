#Problem III: Change in Velocity Calculator
#v = u + at
#U = INITIAL VELOCITY (m/s)
#A = ACCELERATION (m/s^2)
#T = TIME IN SECONDS

U = float(input("ENTER INITIAL VELOCITY: "))
A = float(input("ENTER ACCELERATION: "))
T = float(input("ENTER TIME (IN SECONDS): "))

V = U + A*T

print ("THE FINAL VELOCITY IS: ", V, "m/s")

